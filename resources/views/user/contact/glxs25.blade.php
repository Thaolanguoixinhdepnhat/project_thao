    document.addEventListener('DOMContentLoaded', () => {
  const videoWraps = document.querySelectorAll('.js-video-wrap');

  videoWraps.forEach(wrap => {
    const video = wrap.querySelector('video.common-video__video');
    const playBtn = wrap.querySelector('.js-video-control');
    const cover = wrap.querySelector('.common-video__cover-image');

    if (!video || !playBtn || !cover) return;

    // Khi bấm nút play
    playBtn.addEventListener('click', () => {
      cover.style.opacity = '0';
      cover.style.transition = 'opacity 0.3s ease';
      setTimeout(() => cover.style.display = 'none', 300);

      playBtn.style.display = 'none';
      video.play();
    });

    // Khi video kết thúc
    video.addEventListener('ended', () => {
      cover.style.display = 'block';
      setTimeout(() => cover.style.opacity = '1', 10);
      playBtn.style.display = 'block';
    });

    // Tùy chọn: click video để pause và hiện nút play + cover
    video.addEventListener('click', () => {
      if (!video.paused) {
        video.pause();
        playBtn.style.display = 'block';
        cover.style.display = 'block';
        setTimeout(() => cover.style.opacity = '1', 10);
      }
    });
  });
});

