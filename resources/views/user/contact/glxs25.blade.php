    <script>
        document.addEventListener('DOMContentLoaded', () => {
            const videoBlocks = document.querySelectorAll('.common-video');

            videoBlocks.forEach(block => {
                const video = block.querySelector('.common-video__video');
                const playBtn = block.querySelector('.common-video__play-button');
                const cover = block.querySelector('.common-video__cover-image');
                const progress = block.querySelector('.common-video__control-progress-bar');

                if (!video || !playBtn || !progress) return;

                const radius = 17;
                const circumference = 2 * Math.PI * radius;

                progress.style.strokeDasharray = circumference;
                progress.style.strokeDashoffset = circumference;

                function updateProgress() {
                    if (video.duration) {
                        const percent = video.currentTime / video.duration;
                        progress.style.strokeDashoffset = circumference - percent * circumference;
                    }
                }

                playBtn.addEventListener('click', () => {
                    if (cover) {
                        cover.style.opacity = '0';
                        cover.style.transition = 'opacity 0.5s';
                        setTimeout(() => cover.style.display = 'none', 500);
                    }
                    playBtn.style.display = 'none';
                    video.play();
                });

                video.addEventListener('timeupdate', updateProgress);

                video.addEventListener('ended', () => {
                    playBtn.style.display = 'block';
                    if (cover) {
                        cover.style.display = 'block';
                        cover.style.opacity = '1';
                    }
                    progress.style.strokeDashoffset = circumference;
                });

                video.addEventListener('click', () => {
                    if (!video.paused) {
                        video.pause();
                        playBtn.style.display = 'block';
                        if (cover) {
                            cover.style.display = 'block';
                            cover.style.opacity = '1';
                        }
                    }
                });
            });
        });
